def _validate():
    """Fail import if any language array is misaligned with its canonical 'en'."""
    for name, table in TABLES.items():
        n = len(table["en"])
        for lang, arr in table.items():
            if len(arr) != n:
                raise ValueError(
                    f"translations.{name}[{lang}] has {len(arr)} entries, expected {n}"
                )


